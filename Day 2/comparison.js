console.log("Comparison & Logical Operators");

/*
Comparison
1. Greater than >
2. Less Than <
3. Greaeter than or equal to >=
4. Lesser than or equal to <=
5. Equals to == (This check value only)
6. Not Equals !=
7. Strict Equality === (This checks type and value)


Logical
1. And && - Both the statements should be true
2. OR || - Any one statement should be true

*/


console.log(7 > 6);
console.log(7 <= 6);

